double fof_periodic_wrap(double x)
{
  while(x >= BoxSize)
    x -= BoxSize;

  while(x < 0)
    x += BoxSize;

  return x;
}


double fof_periodic(double x)
{
  if(x >= 0.5 * BoxSize)
    x -= BoxSize;

  if(x < -0.5 * BoxSize)
    x += BoxSize;

  return x;
}


